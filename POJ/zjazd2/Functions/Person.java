package Functions;

public class Person {
    public String firstname;
    public String surname;
    public String field;
    public int group;

    public String getFirstname() {
        return firstname;
    }

    public String getSurname() {
        return surname;
    }

    public String getField() {
        return field;
    }
    public int getGroup() {
        return group;
    }
}



package Functions;

public class PrimitiveTypes {
    short s;
    int i;
    long l;
    float f;
    double d;
    char c;
    String str;
    boolean b;
    public short getS() {
        return s;
    }
    public int getI() {
        return i;
    }
    public long getL() {
        return l;
    }
    public float getF() {
        return f;
    }
    public double getD() {
        return d;
    }
    public char getC() {
        return c;
    }
    public String getStr() {
        return str;
    }
    public boolean getB() {
        return b;
    }

}
